    private BottomNavigationView.OnNavigationItemSelectedListener mOnNavigationItemSelectedListener
            = new BottomNavigationView.OnNavigationItemSelectedListener() {

        @Override
        public boolean onNavigationItemSelected(@NonNull MenuItem item) {
            switch (item.getItemId()) {
                case R.id.navigation_home:
                    textMessage.setText(R.string.title_home);
                    return true;
                case R.id.navigation_dashboard:
                    textMessage.setText(R.string.title_favorites);
                    return true;
                case R.id.navigation_notifications:
                    textMessage.setText(R.string.title_profile);
                    return true;
            }
            return false;
        }
    };

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        textMessage = findViewById(R.id.message);
        recyclerView = findViewById(R.id.item_list);
        BottomNavigationView navigation = findViewById(R.id.navigation);
        navigation.setOnNavigationItemSelectedListener(mOnNavigationItemSelectedListener);

        poseList = new ArrayList<>();
        adapter = new PoseAdapter(this, poseList);

        RecyclerView.LayoutManager layoutManager = new GridLayoutManager(this, 1);
        recyclerView.setLayoutManager(layoutManager);
        //recyclerView.addItemDecoration(new GridSpacingItemDecoration(2, dpToPx(10), true));
        recyclerView.setItemAnimator(new DefaultItemAnimator());
        recyclerView.setAdapter(adapter);

        // TODO: put config around loading test items
        loadTestPoses();
    }

    private void loadTestPoses() {
        for (int i = 0; i < 10; i++) {
            Pose pose = new Pose("Pose"+String.valueOf(i), R.drawable.item1, false);
            poseList.add(pose);
        }

        adapter.notifyDataSetChanged();
    }


